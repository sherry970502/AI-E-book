/**
 * 体裁预设（GenrePreset）——「体裁定骨，风格定皮」。
 *
 * 体裁是一份贯穿全管线的契约，不是一个语气开关：
 *   - tocContract   → 注入目录生成 prompt：章的单位、命名方式、篇幅节奏、递进逻辑
 *   - proseContract → 注入正文生成 system：文体、人称、句长、知识的承载方式
 *   - planHint      → 注入脉络设计 prompt：颗粒序列按什么叙事逻辑组织
 *
 * 不变量：无论什么体裁，学习目标锚定不变——大纲每节挂目标、段落溯源到目标。
 * 体裁改变的是「呈现方式」，不是「目标驱动」。
 *
 * MVP 只做 prompt 层。后续第二步：per-genre 颗粒词表；第三步：BookPage 渲染变体。
 */

export type GenreId = 'textbook' | 'picture-book' | 'novel' | 'bestseller'

export interface GenrePreset {
  id: GenreId
  label: string
  emoji: string
  desc: string
  /** 创建表单里的示例预览（同一主题在该体裁下的开篇样貌） */
  sample: string
  /** 目录生成契约：为空 = 沿用默认教科书行为 */
  tocContract: string
  /** 正文生成契约：为空 = 沿用默认教科书行为 */
  proseContract: string
  /** 脉络设计提示：为空 = 沿用默认教学递进 */
  planHint: string
}

